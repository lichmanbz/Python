# Функция для проверки четности числа
def check_even_odd():
    user_input = input("Введите число: ")
    
    try:
        # Пробуем преобразовать ввод в целое число
        number = int(user_input)

        # Проверяем, что число положительное
        if number < 0:
            print("Ошибка: введите положительное целое число.")
            return

        # Проверяем четность
        if number % 2 == 0:
            print(f"Число {number} является четным.")
        else:
            print(f"Число {number} не является четным.")
    
    except ValueError:
        # Обработка ошибки, если ввод не является числом
        print("Ошибка: введено не число.")

# Запуск функции
check_even_odd()
