# Функция для проверки совершеннолетия
def check_age():
    user_input = input("Введите ваш возраст: ")
    
    try:
        # Пробуем преобразовать ввод в целое число
        age = int(user_input)

        # Проверяем, что возраст не отрицательный
        if age < 0:
            print("Ошибка: возраст не может быть отрицательным!")
            return

        # Проверяем совершеннолетие
        if age >= 18:
            print("Вы совершеннолетний.")
        else:
            print("Вы несовершеннолетний.")
    
    except ValueError:
        # Обработка ошибки, если ввод не является числом
        print("Ошибка: введено не число!")

# Запуск функции
check_age()
